    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:

        String validMsg = formValidation();
        if (validMsg == null) {
            if (!db.insert(new Administrasi(-1, null, -1, keterangan, nominal))) {
                JOptionPane.showMessageDialog(null, db.getMessage());
            }
            db.closeConnection();
            Main.newForm.showTableAdmin(null);//update Table
            reset();
            JOptionPane.showMessageDialog(null, keterangan + " Berhasil dimasukkan!");
        } else {
            JOptionPane.showMessageDialog(null, validMsg);
        }


    }//GEN-LAST:event_btnOKActionPerformed

