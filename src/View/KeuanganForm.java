    private String formValidation() {

        keterangan = txtKeterangan.getText();
        try {
            nominal = Double.parseDouble(txtNominal.getText());
        } catch (Exception e) {
            return "Isi form dengan lengkap!";
        }

        if (keterangan.equals("")) {
            return "Jenis kosong!";
        }

        if (txtNominal.getText().equals("")) {
            return "Nominal kosong!";
        }

        return null;
    }

    private void reset() {
        txtNominal.setText("");
        txtKeterangan.setText("");
    }

